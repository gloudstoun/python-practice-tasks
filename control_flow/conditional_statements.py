user_age = input("Введите свой возраст: ")
user_age_int = int(user_age)
# Улучшенная проверка недопустимых значений
if user_age_int < 0 or user_age_int > 120: # Возраст меньше 0 или больше 120 (условный максимум)
    print("Недопустимое значение возраста. Пожалуйста, введите корректный возраст.")
else:
    # Здесь идут ваши основные проверки, если возраст допустим
    if user_age_int >= 18:
        print("Вы взрослый.")
    elif user_age_int >= 13: # Условие user_age_int <= 17 не нужно, т.к. если не >= 18, то точно < 18
        print("Вы подросток.")
    else: # Если не >= 18 и не >= 13, значит < 13
        print("Вы ребенок.")

